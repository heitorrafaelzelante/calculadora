    private boolean verificaLetra(char c) {
        String bancoDados = "0123456789,+-*/";
        if (bancoDados.contains(String.valueOf(c))) {
            return true;
        }
        return false;
    }

    private void insereVirgula() {
        if (jtfVisor.getText().isEmpty()) {
            setNumeroVisor("0,");
        } else if (!jtfVisor.getText().contains(",")) {
            setNumeroVisor(",");
        }
    }

    private void apagarTudo() {
        if (jtfVisor.getText().isEmpty()) {
        } else {
            jtfVisor.setText(null);
            operacao = "";
        }

    }

    private void apagar() {
        String apagar = jtfVisor.getText();
        StringBuilder back = new StringBuilder(apagar);
        if (!jtfVisor.getText().isEmpty()) {
            apagar = String.valueOf(back.deleteCharAt(apagar.length() - 1));
            jtfVisor.setText(apagar);
        }
    }

    private void calculo() {
        try{
        if (jtfVisor.getText().isEmpty()) {
        } else {
            info2 = Double.parseDouble(jtfVisor.getText().replaceAll(",", "."));
            if (operacao.equals("+")) {
                jtfVisor.setText(String.valueOf(info1 + info2));
            }
            if (operacao.equals("-")) {
                jtfVisor.setText(String.valueOf(info1 - info2));
            }
            if (operacao.equals("/")) {
                jtfVisor.setText(String.valueOf(info1 / info2));
            }
            if (operacao.equals("x")) {
                jtfVisor.setText(String.valueOf(info1 * info2));
            }
            operacao = "";
            info1 = Double.parseDouble(jtfVisor.getText().replaceAll(",", "."));
            podeApagar = true;
        }
    }catch(NumberFormatException e){
        }
    }

    private void preCalculo(String op) {
        if (jtfVisor.getText().isEmpty()) {
        } else {
            if (operacao.equals("")) {
                info1 = Double.parseDouble(jtfVisor.getText().replaceAll(",", "."));
                operacao = op;
                setNumeroVisor(op);
                podeApagar = true;
            } else {
                String lastChar = jtfVisor.getText();
                lastChar = lastChar.substring(lastChar.length() -1, lastChar.length());
                if (lastChar.contains("+ / - x")) {
                    apagar();
                    info1 = Double.parseDouble(jtfVisor.getText().replaceAll(",", "."));
                    operacao = op;
                    podeApagar = false;
                    setNumeroVisor(op);
                    podeApagar = true;
                } else {
                    calculo();
                    operacao = op;
                }
            }
        }
    }

