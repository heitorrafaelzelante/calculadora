    private void jtfVisorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfVisorKeyTyped
        char c = evt.getKeyChar();
        if (!verificaLetra(c)) {
            evt.consume();
        } else {
            if (c == '*') {
                c = 'x';
            }
            if (c == ',') {
                insereVirgula();
                evt.consume();
            } else {
                String textoPronto = jtfVisor.getText() + c;
                jtfVisor.setText(textoPronto);
                evt.consume();
            }
            if (jtfVisor.getText().isEmpty()) {
                preCalculo(operacao);
                calculo();

            }

        }


    }//GEN-LAST:event_jtfVisorKeyTyped

